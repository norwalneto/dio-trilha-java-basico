// Interface para Navegador na Internet
// Esta interface define os métodos relacionados à navegação na web.
public interface NavegadorInternet {
    void exibirPagina(String url); // Exibe uma página web através de uma URL
    void adicionarNovaAba(); // Adiciona uma nova aba ao navegador
    void atualizarPagina(); // Atualiza a página atualmente aberta
}
